e_Ret level_1_Loop(e_State * eNextState)
{
    e_Ret eRet = RET_OK;
    eNextState = eNextState; //Unref parameter

    do{
        control_HandleEvents();
        screen_Update();
        eRet = screen_Render();
        if (eRet){
            printf("Nao foi possivel renderizar a tela!\n");
            break;
        }
        
    } while (control_GetRunning());

    return eRet;
}
